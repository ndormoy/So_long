#include "libft.h"

void	ft_lstadd_back(t_list **alst, t_list *new)
{
	t_list	*last;

	if (!*alst)
		*alst = new;
	else if (!new)
		return ;
	else
	{
		last = ft_lstlast(*alst);
		last ->next = new;
		new ->next = NULL;
	}
}
