#include "libft.h"

char	*ft_strdup(const char *src)
{
	char			*dest;
	int				len;
	unsigned int	i;

	i = 0;
	len = ft_strlen ((char *)src);
	dest = malloc(sizeof(char) * len + 1);
	if (!dest)
		return (NULL);
	while (src[i])
	{
		dest[i] = src[i];
		i++;
	}
	dest[i] = '\0';
	return (dest);
}
