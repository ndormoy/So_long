#include "libft.h"
#include <stdio.h>

int	ft_atoi(const char *str)
{
	unsigned int	atoi;
	int				neg;

	neg = 1;
	atoi = 0;
	while ((*str >= 9 && *str <= 13) || *str == ' ')
		str++;
	if (*str == '+' || *str == '-')
	{
		if (*str == '-')
			neg *= -1;
		str++;
	}
	while (*str >= '0' && *str <= '9' && *str)
	{
		atoi = atoi * 10 + *str - 48;
		str++;
	}
	return (atoi * neg);
}
